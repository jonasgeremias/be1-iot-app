import { QueryClient } from '@tanstack/react-query';

/** Shared React Query client. All server data flows through this cache. */
export const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      staleTime: 30_000,
      gcTime: 5 * 60_000,
      retry: 1,
      refetchOnWindowFocus: false,
    },
  },
});
